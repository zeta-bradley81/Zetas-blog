@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=form.email.data).first():
            # print(User.query.filter_by(email=form.email.data))
            flash ("This is email address has already been registered. Please log in")
            return redirect(url_for('login'))
        else:
            password = form.password.data
            hashed_password = generate_password_hash(password=password)
            new_user = User(
                email=form.email.data,
                name=form.name.data,
                password=hashed_password,
            )
            db.session.add(new_user)
            db.session.commit()
            login_user(new_user)
            # flash ('You have been successfully registered.')
            return redirect(url_for('get_all_posts'))

    return render_template("register.html", form=form)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=form.email.data).first():
            user=User.query.filter_by(email=form.email.data).first()
            if user and check_password_hash(user.password, form.password.data):
                login_user(user)
                return redirect(url_for("get_all_posts"))
            else:
                flash("Incorrect password. Please re-enter your credentials.")
                return redirect(url_for('login'))
        else:
            flash("No account matches this address.")
            return redirect(url_for('login'))

    return render_template("login.html", form=form)


